/**
 * Compound assignment with augmented operations
 */

 // Addition
 var myVar = 1;
 myVar += 5; // assigned 6

 // Subtraction
 var myOtherVar = 11;
 myOtherVar -= 6; // assigned 7

 // Multiplication
 var myMultiVar = 5;
 myMultiVar *= 2; // assigned 10

 // Division;
 var myDividedVar = 18;
 myDividedVar /= 3; // assigned 6